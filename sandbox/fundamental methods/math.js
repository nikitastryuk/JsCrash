//Возвращает абсолютное значение числа
console.log(Math.abs(-2.43)); //2.43

//Возвращает наименьшее целое число, большее, либо равное указанному числу.
console.log(Math.ceil(0.4)); //1

//Возвращает наименьшее целое число, меньшее, либо равное указанному числу
console.log(Math.floor(1.9)); //1


console.log(Math.max(2,3,6,8,43,2)); //43

console.log(Math.min(2,3,6,8,43,2)); //2

console.log(Math.pow(2,3)); //8

//Возвращает псевдослучайное число в диапазоне от 0 до 1
console.log(Math.random());

//Возвращает значение числа, округлённое до ближайшего целого
console.log(Math.round(1.5)) //2

console.log(Math.sign(-2)); //-1

console.log(Math.sqrt(64)); //8